def load_model(repo):
    model = AutoModelForCausalLM.from_pretrained(repo, dtype=torch.bfloat16, device_map="auto")
    tok_file = hf_hub_download(repo, "tokenizer.json")
    tokenizer = PreTrainedTokenizerFast(tokenizer_file=tok_file)
    tokenizer.pad_token_id = 1
    model.requires_grad_(False)
    return model, tokenizer


def correct(model, tokenizer, text, **gen_kwargs):
    prompt = f"### Нұсқау:\n{INSTRUCTION}\n\n### Мәтін:\n{text}\n\n### Түзетілген:\n"
    ids = tokenizer.encode(prompt, return_tensors="pt").to(model.device)
    attn = torch.ones_like(ids)
    with torch.no_grad():
        out = model.generate(
            ids, attention_mask=attn,
            max_new_tokens=min(ids.shape[1], 512),
            pad_token_id=1, **gen_kwargs,
        )
    decoded = tokenizer.decode(out[0], skip_special_tokens=True)
    return decoded.split("### Түзетілген:\n")[-1].split("###")[0].strip()


def check(output, expected):
    o = output.strip().rstrip(".")
    e = expected.strip().rstrip(".")
    return o == e


def run_tests(model, tokenizer, settings_dict):
    results = {}
    for sname, sparams in settings_dict.items():
        print(f"    Setting {sname}...", end=" ", flush=True)
        t0 = time.time()
        passed = 0
        details = []
        for inp, exp, cat in TESTS:
            out = correct(model, tokenizer, inp, **sparams)
            ok = check(out, exp)
            if ok:
                passed += 1
            details.append({
                "input": inp, "expected": exp, "output": out,
                "category": cat, "pass": ok,
            })
        elapsed = time.time() - t0
        pct = round(100 * passed / len(TESTS), 1)
        results[sname] = {
            "score": passed, "total": len(TESTS),
            "pct": pct, "time_s": round(elapsed, 1),
            "details": details,
        }
        print(f"{passed}/{len(TESTS)} ({pct}%) in {elapsed:.1f}s")
    return results


def print_summary(all_results):
    print("\n" + "=" * 90)
    print("OVERALL SCORES")
    print("=" * 90)

    settings_names = list(list(all_results.values())[0].keys())
    header = f"{'Model':<6} | " + " | ".join(f"{s:<16}" for s in settings_names)
    print(header)
    print("-" * len(header))

    for mname, sresults in all_results.items():
        row = f"{mname:<6} | "
        row += " | ".join(
            f"{r['score']:>3}/{r['total']} ({r['pct']:>5.1f}%)"
            for r in sresults.values()
        )
        print(row)

    # Per-category breakdown for each setting
    print("\n" + "=" * 90)
    print("PER-CATEGORY BREAKDOWN (all settings)")
    print("=" * 90)

    categories = []
    seen = set()
    for _, _, cat in TESTS:
        if cat not in seen:
            categories.append(cat)
            seen.add(cat)

    for mname, sresults in all_results.items():
        print(f"\n--- {mname} ---")
        cat_header = f"{'Category':<12} | " + " | ".join(f"{s:<16}" for s in settings_names)
        print(cat_header)
        print("-" * len(cat_header))
        for cat in categories:
            row = f"{cat:<12} | "
            for sname in settings_names:
                details = sresults[sname]["details"]
                cat_tests = [d for d in details if d["category"] == cat]
                cat_pass = sum(1 for d in cat_tests if d["pass"])
                row += f"{cat_pass:>3}/{len(cat_tests):<13} | "
            print(row)

    # Failing examples for default setting
    print("\n" + "=" * 90)
    print("FAILING EXAMPLES (A_default setting)")
    print("=" * 90)
    for mname, sresults in all_results.items():
        details = sresults["A_default"]["details"]
        fails = [d for d in details if not d["pass"]]
        print(f"\n{mname} — {len(fails)} failures:")
        for d in fails:
            print(f"  [{d['category']:<10}] {d['input']}")
            print(f"    GOT: {d['output']}")
            print(f"    EXP: {d['expected']}")


def main():
    all_results = {}

    for mname, mrepo in MODELS.items():
        print(f"\n{'='*60}")
        print(f"Loading {mname}: {mrepo}")
        print(f"{'='*60}")
        model, tokenizer = load_model(mrepo)

        results = run_tests(model, tokenizer, SETTINGS)
        all_results[mname] = results

        del model, tokenizer
        gc.collect()
        torch.cuda.empty_cache()

    print_summary(all_results)

    # Save full results
    with open("/root/gec_eval_100.json", "w") as f:
        save_data = {}
        for mname, sresults in all_results.items():
            save_data[mname] = {}
            for sname, r in sresults.items():
                save_data[mname][sname] = {
                    "score": r["score"], "total": r["total"],
                    "pct": r["pct"], "time_s": r["time_s"],
                    "details": r["details"],
                }
        json.dump(save_data, f, ensure_ascii=False, indent=2)
    print("\nFull results saved to /root/gec_eval_100.json")


if __name__ == "__main__":
    main()
